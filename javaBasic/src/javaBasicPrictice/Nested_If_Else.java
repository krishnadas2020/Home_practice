package javaBasicPrictice;

public class Nested_If_Else {

	public static void main(String[] args) {
		
/*nested condition will check every statement, if any statements are true then  the condition  will break
 * if non of statements are not  true then nested else will executed  */
		
		char ch = 'h';
		if(ch == 'a') {
			System.out.println("apple");
		}else if(ch == 'b') {
			System.out.println("banana");
			
		}else if(ch == 'c') {
			System.out.println("charry");
			
		}else {
			System.out.println("unknown to me");
			
		}

		
		
		
		
		
		
		
		
		
		
		
		
	}	
}


